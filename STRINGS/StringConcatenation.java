public class StringConcatenation {

    public static void main(String[] args) {
        
        // Defining two strings
        String str1 = "Hello";
        String str2 = "World";

        // Concatenating strings using + operator
        String result = str1 + " " + str2;

        // Printing the result
        System.out.println("Concatenated String: " + result);
    }
}
